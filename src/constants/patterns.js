export const REGEX_PATTERNS = {
  email: /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  phoneNo: /^\+([0-9]{1,3})\)?([0-9]{8,10})$/,
  startsWith91: /^\+(91)/,
  indianPhoneNo: /^\+(91)[6789]\d{9}$/,
  zip: /^[0-9]{3,10}$/,
  aadhar: /^[2-9]{1}[0-9]{3}[0-9]{8}$/, // VALID: (499118665246)
  passport: /^[a-zA-Z0-9_-]{5,10}$/, // VALID: (A12 34567 or S1234567)
  pan: /[A-Z]{5}[0-9]{4}[A-Z]{1}$/, // VALID: (ALWPG5809L)
};
